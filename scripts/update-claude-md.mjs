#!/usr/bin/env node
/**
 * Regenerate the auto-maintained inventory block in CLAUDE.md.
 *
 * Scans the repo (routes, API endpoints, components, lib, generated media, docs)
 * and rewrites the content between <!-- AUTO:START --> and <!-- AUTO:END -->.
 * Idempotent: only writes when the block actually changed, so it's safe to run
 * from a Stop hook on every turn (no timestamp = no spurious diffs).
 *
 * Usage: node scripts/update-claude-md.mjs
 */
import { readFileSync, writeFileSync, readdirSync, existsSync, statSync } from "node:fs";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const WEB = join(ROOT, "web");
const APP = join(WEB, "src/app");
const TARGET = join(ROOT, "CLAUDE.md");
const START = "<!-- AUTO:START -->";
const END = "<!-- AUTO:END -->";

function walk(dir, hit, out = []) {
  if (!existsSync(dir)) return out;
  for (const name of readdirSync(dir)) {
    const p = join(dir, name);
    const s = statSync(p);
    if (s.isDirectory()) walk(p, hit, out);
    else if (hit(p)) out.push(p);
  }
  return out;
}

function routeFor(file) {
  // web/src/app/foo/bar/page.tsx -> /foo/bar ; app/page.tsx -> /
  const rel = relative(APP, dirname(file)).split("\\").join("/");
  return rel === "" ? "/" : "/" + rel;
}

function count(dir) {
  return existsSync(dir) ? readdirSync(dir).filter((f) => !f.startsWith(".")).length : 0;
}

function list(dir, ext) {
  return existsSync(dir)
    ? readdirSync(dir).filter((f) => f.endsWith(ext)).map((f) => f.replace(ext, "")).sort()
    : [];
}

// ---- pages (routes) ----
const pages = walk(APP, (p) => p.endsWith("/page.tsx"))
  .map(routeFor)
  .filter((r) => !r.startsWith("/api"))
  .sort();

// ---- api endpoints ----
const apis = walk(join(APP, "api"), (p) => p.endsWith("/route.ts"))
  .map((f) => "/api/" + relative(join(APP, "api"), dirname(f)).split("\\").join("/"))
  .sort();

// ---- components & lib ----
const components = list(join(WEB, "src/components"), ".tsx");
const libs = list(join(WEB, "src/lib"), ".ts");

// ---- hotel catalogue exports ----
let catalogues = [];
const hotelFile = join(WEB, "src/lib/hotel.ts");
if (existsSync(hotelFile)) {
  catalogues = [...readFileSync(hotelFile, "utf8").matchAll(/export const ([A-Z_]+)\b/g)]
    .map((m) => m[1]);
}

// ---- generated media ----
const pub = join(WEB, "public");
const media = {
  luxe: count(join(pub, "property/luxe")),
  neon: count(join(pub, "property/neon")),
  personas: count(join(pub, "personas")),
  hero: count(join(pub, "hero")),
};

// ---- docs ----
const docs = list(join(ROOT, "docs"), ".md");

// ---- build block ----
const lines = [
  START,
  "<!-- Generated by scripts/update-claude-md.mjs (Stop hook). Do not edit between the AUTO markers. -->",
  "",
  `### Pages (${pages.length})`,
  pages.map((r) => `- \`${r}\``).join("\n"),
  "",
  `### API endpoints (${apis.length})`,
  apis.map((r) => `- \`${r}\``).join("\n"),
  "",
  `### Components (${components.length})`,
  components.map((c) => `\`${c}\``).join(" · "),
  "",
  `### Library modules (\`web/src/lib\`)`,
  libs.map((l) => `\`${l}\``).join(" · "),
  "",
  `### Hotel catalogues (\`lib/hotel.ts\` exports)`,
  catalogues.map((c) => `\`${c}\``).join(" · "),
  "",
  `### Generated media (\`web/public\`)`,
  `- property/luxe: **${media.luxe}** · property/neon: **${media.neon}** · personas: **${media.personas}** · hero: **${media.hero}**`,
  "",
  `### Docs (\`docs/\`)`,
  docs.length ? docs.map((d) => `- [\`docs/${d}.md\`](docs/${d}.md)`).join("\n") : "- (none)",
  END,
].join("\n");

if (!existsSync(TARGET)) {
  console.error("CLAUDE.md not found — nothing to update.");
  process.exit(0);
}

const current = readFileSync(TARGET, "utf8");
const re = new RegExp(`${START}[\\s\\S]*?${END}`);
if (!re.test(current)) {
  console.error("AUTO markers not found in CLAUDE.md — leaving file untouched.");
  process.exit(0);
}

const next = current.replace(re, lines);
if (next !== current) {
  writeFileSync(TARGET, next);
  console.log("CLAUDE.md inventory updated.");
} else {
  console.log("CLAUDE.md inventory already current.");
}
